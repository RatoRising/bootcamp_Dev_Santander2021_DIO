// função para gerar tabuadas
var tabDo = 8
var limite = 10
function tabuada() {
    for (let cont = 1; cont <= limite; cont++) {
        console.log(`9 x ${cont} = ${cont * tabDo}`)
    }
}
console.log(tabuada())


